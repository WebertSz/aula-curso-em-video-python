def titulo(txt):
    print('-=' * 20)
    print(txt)
    print('-=' * 20)


def escreva(msg):
    print(f'-' * len(msg))
    print(msg)
    print(f'-' * len(msg))


titulo("FUNÇÃO - PASSANDO UMA MENSAGEM DE TEXTO")
escreva("Curso de Python no Youtube")
escreva("Professor: Guanabara")
escreva("Curso em vídeo")

